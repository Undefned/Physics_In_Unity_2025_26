using UnityEngine;

// OpticalMedium описывает оптические параметры конкретного объекта-среды.
// Скрипт ставится на объект с Collider (например Cube), который играет роль
// границы раздела сред в трассировке луча.
public class OpticalMedium : MonoBehaviour
{
    [Header("Название среды")]
    public string mediumName = "Glass";

    [Header("Показатель преломления внутри объекта")]
    // n_inside: показатель преломления материала объекта (внутри коллайдера).
    // Примеры: воздух ~1.0, вода ~1.33, стекло ~1.5.
    [Min(1f)] public float refractiveIndex = 1.5f;

    [Header("Показатель преломления снаружи")]
    // n_outside: показатель среды вокруг объекта.
    // Обычно 1.0 (воздух), если объект расположен в воздухе.
    // Нужен для корректного расчета выхода луча из объекта.
    [Min(1f)] public float externalRefractiveIndex = 1.0f;

    [Header("Коэффициент поглощения alpha (1/м)")]
    // alpha в законе Бугера-Ламберта-Бера:
    // I = I0 * exp(-alpha * L), где L — путь в среде.
    // Чем alpha больше, тем быстрее луч теряет интенсивность.
    [Min(0f)] public float absorption = 0.0f;
}
